# Program Menhitung Luas Segitiga
# I.S. : Pengguna memasukan alas segitiga (A) dan tinggi segitiga (T)
# F.S. : Menampilkan hasil perhitungan luas segitiga

# Input
# Memasukan alas dan tinggi
A = int(input("Masukan Alas Segitiga   : "))
T = int(input("Masukan Tinggi Segitiga : "))

# Proses
# Menghitung luas segitiga
LuasSegitiga = 0.5 * A * T

# Output
# Menampilkan luas segitiga
print("Luas Segitiga : ", LuasSegitiga)
