# Program Menghitung Keliling Lingkaran
# I.S. : Pengguna memasukan diameter lingkaran (D) dalam satuan Meter
# F.S. : Menampilkan hasil perhitungan keliling lingkaran dalam satuan Cm

# Memasukan diameter lingkaran
D = int(input("Masukan Diameter Lingkaran : "))

# Menghitung keliling lingkaran
KelilingLingkaran = (3.14 * D) * (100)

# Menampilkan keliling lingkaran
print("Keliling Lingkaran : ", KelilingLingkaran, "Cm")
