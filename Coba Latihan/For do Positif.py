# Program Menjumlahkan Angka 1 Sampai 10
# I.S. : Diberikan harga pencacah (i) = 10
# F.S. : Menampilkan hasil penjumlahan 1+2..+10

Hasil = 0

for i in range(10):
    Hasil = Hasil + i
    
print(Hasil)

